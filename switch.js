let today= new Date().getDay()
switch(today){
    case 1:
        console.log('monday')
        break;
        case 2:
            console.log('saturday')
            break;
            case 3:
                console.log('sunday')
                break;
                case 4:
                    console.log('tuesday')
                    break;
                    case 5:
                        console.log('friday')
                        break;
                        case 6:
                            console.log('thursday')
                            break;
                            case 7:
                                console.log('wednesday')
                                break;
            default:
                console.log('num not found')
}